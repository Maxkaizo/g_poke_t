def load_csv(path):
    print(f"ğŸ“‚ Leyendo {path}...")
    df = pd.read_csv(path)
    return df

def insert_collection(df, collection_name):
    """Inserta los documentos del DataFrame en MongoDB"""
    print(f"ğŸ’¾ Insertando en colecciÃ³n '{collection_name}' ({len(df)} documentos)...")
    records = df.to_dict(orient="records")
    db[collection_name].delete_many({})  # limpia colecciÃ³n previa
    db[collection_name].insert_many(records)
    print(f"âœ… {collection_name}: {db[collection_name].count_documents({})} registros totales.")

